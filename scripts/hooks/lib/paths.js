/**
 * Shared path utilities for hook scripts.
 *
 * Solves the Windows path normalization bug: CLAUDE_PROJECT_DIR may be
 * unset (defaults to "."), but Claude Code sends absolute Windows paths
 * like C:\Users\...\file.ts. Without path.resolve(), startsWith() fails
 * and all path-based matching breaks silently.
 *
 * Usage:
 *   const { PROJECT, relPath } = require("./lib/paths");
 */

const path = require("path");
const fs = require("fs");

const PROJECT = path.resolve(process.env.CLAUDE_PROJECT_DIR || ".");

// ── Centralized path registry ──────────────────────────────
// All hooks read paths from .claude/paths.json (generated by scripts/paths/build.js
// from warpos/paths.registry.json). To move a folder, edit the registry, run
// build.js, commit all generated artifacts together. The fallback below now
// loads from paths.generated.js (also built by the same generator) so the
// hand-duplicated table that used to live here is closed as a source of fork.
let PATHS = {};
try {
  const raw = JSON.parse(
    fs.readFileSync(path.join(PROJECT, ".claude", "paths.json"), "utf8"),
  );
  PATHS = Object.fromEntries(
    Object.entries(raw)
      .filter(([k]) => k !== "version" && k !== "$schema")
      .map(([k, v]) => [k, path.join(PROJECT, v)]),
  );
} catch {
  // Fallback path 1: load from paths.generated.js (built by scripts/paths/build.js
  // from warpos/paths.registry.json). Same shape as paths.json but ships in the
  // hook lib so paths resolve even if .claude/paths.json was deleted.
  try {
    PATHS = require("./paths.generated").PATHS;
  } catch {
    // Fallback path 2: hardcoded defaults. Only hit if both paths.json AND
    // paths.generated.js are absent (e.g. fresh install before first build).
    // Keep this small subset alive for the bare-bootstrap case.
    PATHS = LEGACY_FALLBACK_PATHS();
  }
}

